function getStoredLanguage() {
    return localStorage.getItem('kv-lang') || 'en';
}

function applyLanguage(lang) {
    const dict = KV_TRANSLATIONS[lang];
    if (!dict) return;

    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = dict[key];
        if (text === undefined) return;

        if (el.hasAttribute('data-i18n-html')) {
            el.innerHTML = text;
        } else if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = text;
        } else if (el.tagName === 'OPTION') {
            el.textContent = text;
        } else {
            el.textContent = text;
        }
    });

    const pageTitleKey = document.body.getAttribute('data-page-title');
    if (pageTitleKey && dict[pageTitleKey]) {
        document.title = dict[pageTitleKey];
    }

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    localStorage.setItem('kv-lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const lang = getStoredLanguage();
    applyLanguage(lang);

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => applyLanguage(btn.dataset.lang));
    });
});
